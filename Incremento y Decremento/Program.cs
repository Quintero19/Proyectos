//Operadores de incremento y decremento 
int Valor = 7;
Console.WriteLine("Valor original: " +  Valor);
Valor++;
Console.WriteLine("Valor Incrementando: " + Valor);

//Valor decremento
int Valor2 = 4;
Console.WriteLine("Valor original: " + Valor2);
Valor2--;
Console.WriteLine("Valor decrementado: " + Valor2);

//Valor incrementado por cantidad ingresada
int Valor3 = 8;
Console.WriteLine("Valor original: " + Valor3);
Valor3 += 3;
Console.WriteLine("Valor incrementado: " + Valor3);


//Valor Decrementado por cantidad ingresada
int Valor4 = 5;
Console.WriteLine("Valor original: " + Valor4);
Valor4 -= 2;
Console.WriteLine("Valor decrementado: " + Valor4);

