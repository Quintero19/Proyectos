int a = 20, b = 30;
string resultado;
//Mayor que

resultado = (a > b) ? "SI" : "NO";
Console.WriteLine(resultado);

//Menor que
resultado = (a < b) ? "SI" : "NO";
Console.WriteLine(resultado);

//Mayor o igual que
resultado = (a >= b) ? "SI" : "NO";
Console.WriteLine(resultado);

//Menor o igual que
resultado = (a <= b) ? "SI" : "NO";
Console.WriteLine(resultado);

//Igual o Igual a que
resultado = (a == b) ? "SI" : "NO";
Console.WriteLine(resultado);

//Distinto que
resultado = (a != b) ? "SI" : "NO";
Console.WriteLine(resultado);
