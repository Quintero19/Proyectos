int numero1 = 30, numero2 = 20, numero3 = 50;
bool resultado;


//Operador logico AND
resultado = numero1 < numero3 && numero2 < numero1;
Console.WriteLine(resultado);

// Operador Logico OR
resultado = numero3 < numero1 || numero1 > numero2;
Console.WriteLine(resultado);

// Operador Logico NOT
resultado = numero3 < numero1 || numero1 > numero2;
Console.WriteLine(!resultado);

